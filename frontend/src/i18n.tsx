const dicts: Record<Lang, Record<Key, string>> = { en, km }

function format(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? String(vars[k]) : m))
}

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (k: Key, vars?: Record<string, string | number>) => string }
const LangContext = createContext<Ctx>({ lang: 'en', setLang: () => undefined, t: (k, v) => format(en[k], v) })

export function LangProvider(props: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem('lang') as Lang) ?? 'en')
  useEffect(() => {
    // Chrome follows the language: km redefines the UI face + breathing room
    // via tokens (see index.css [data-lang="km"]); data stays on .khmer-content.
    document.documentElement.dataset.lang = lang
  }, [lang])
  const setLang = (l: Lang) => {
    localStorage.setItem('lang', l)
    setLangState(l)
  }
  const t = (k: Key, vars?: Record<string, string | number>) => format(dicts[lang][k] ?? en[k], vars)
  return <LangContext.Provider value={{ lang, setLang, t }}>{props.children}</LangContext.Provider>
}

export function useT() {
  return useContext(LangContext)
}
